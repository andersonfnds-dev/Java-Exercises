package fundamentos;

public class Inferência {

	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);

// var idenfere automaticamente o tipo da variável
		
		var b = 4.5;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
	}
}
